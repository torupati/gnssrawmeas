def parse_sample_csv_text(csv_text: str) -> list[MeasurementForCarrierSmoothing]:
    reader = csv.DictReader(StringIO(csv_text.strip()))
    rows: list[MeasurementForCarrierSmoothing] = []
    for row in reader:
        rows.append(
            MeasurementForCarrierSmoothing(
                datetime=datetime.strptime(row["datetime"], "%Y-%m-%d %H:%M:%S"),
                PR=float(row["PR_L1"]),
                CP=float(row["CP_L1"]),
            )
        )
    return rows


def plot_pseudorange(
    rows: list[MeasurementForCarrierSmoothing],
    data_smoothed: list[MeasurementForCarrierSmoothing],
) -> None:
    rows_sorted = sorted(rows, key=lambda r: r.datetime)
    times = [row.datetime for row in rows_sorted]
    pr_values = [row.PR for row in rows_sorted]
    pr_smoothed = [row.PR for row in data_smoothed]
    pr_diff = [pr - pr_s for pr, pr_s in zip(pr_values, pr_smoothed)]

    fig, axs = plt.subplots(2, 1, figsize=(10, 4), sharex=True)
    axs[0].plot(times, pr_values, marker="o", linewidth=1, markersize=2, label="PR_L1")
    axs[0].plot(times, pr_smoothed, linewidth=1.5, label="Smoothed PR")
    # axs[0].set_xlabel("Time")
    axs[0].set_ylabel("Pseudorange (m)")
    axs[0].set_title("Pseudorange vs Time")
    axs[0].grid(True, linestyle="--", alpha=0.4)

    axs[1].plot(
        times, pr_diff, marker="o", linewidth=1, markersize=2, label="PR - Smoothed PR"
    )
    axs[1].set_xlabel("Time")
    # plt.legend()
    # plt.gcf().autofmt_xdate()
    # plt.tight_layout()
    plt.show()


def test_smooth_code_carrier():
    data_rows = parse_sample_csv_text(sample_csv_string)
    rows_sorted = sorted(data_rows, key=lambda r: r.datetime)
    data_input = [
        MeasurementForCarrierSmoothing(datetime=row.datetime, PR=row.PR, CP=row.CP)
        for row in rows_sorted
    ]
    data_filtered = smooth_code_carrier(rows_sorted, wlen_L1)
    plot_pseudorange(data_input, data_filtered)
